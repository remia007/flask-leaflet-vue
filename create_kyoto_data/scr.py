def main():

    l = []
    for nm, url in sample_dict.items():
        site = requests.get(url)
        text = site.text

        # polygonがない、もしくはURLが存在しない場合
        if text.find('POLYGON') == -1:
            # ポリゴンがからの場合geoJSONで表示する際エラーで落ちるので、csvデータからは削除する
            l.append([nm, ""])
            continue
        
        # 一回目のダブルコーテーションのインデックス番号
        index = text.find('POLYGON') - 1
        # "POLYGON~より前を削除
        dammy_polygon = text[index:]
        # POLYGON~(ダブルコーテーションはいれない)
        secound_target = dammy_polygon[1:]
        # 二回目ダブルコーテーション
        secound_index = secound_target.find('\"')

        # ダブルコーテーションを入れてない分はたす
        polygon = dammy_polygon[0:secound_index+2]

        l.append([nm, polygon])

    df = pd.DataFrame(l)
    df.to_csv('kamigyo.csv', encoding='utf-8')

if __name__ == '__main__':
    main()

